export async function getCountries(): Promise<Country[]> {
  try {
    const res = await fetch(
      "https://restcountries.com/v2/all?fields=name,flag"
    );
    const countries: Country[] = await res.json();
    return countries;
  } catch {
    throw new Error("Could not fetch countries");
  }
}

